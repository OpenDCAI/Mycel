import { expect, test } from "playwright/test";

const MONITOR_TOKEN_KEY = "leon-monitor-token";

test.beforeEach(async ({ page }) => {
  await page.addInitScript((tokenKey) => {
    window.localStorage.setItem(tokenKey, "token-1");
  }, MONITOR_TOKEN_KEY);
});

test("creates a benchmark batch and opens trace-rich run detail", async ({ page }) => {
  await page.goto("/evaluation");

  await expect(page.getByRole("heading", { level: 1, name: "Evaluation" })).toBeVisible();
  await page.getByLabel("Agent user id").fill("agent-1");
  await page.getByLabel("Family").selectOption("SWE-bench Verified");
  await page.getByLabel("Judge profile").selectOption("swe_verified_smoke");
  await page.getByLabel("Export profile").selectOption("predictions_jsonl");
  await page.getByRole("button", { name: /swe_verified_pytest_7521/i }).click();
  await page.getByRole("button", { name: "Create batch" }).click();

  await expect(page).toHaveURL(/\/evaluation\/batches\/eval-batch-created-\d+$/);
  await expect(page.getByRole("heading", { name: /Evaluation Batch eval-batch-created-/ })).toBeVisible();
  await expect(page.getByText("SWE-bench Verified").first()).toBeVisible();

  await page.getByRole("button", { name: "Start evaluation batch" }).click();
  await expect(page.getByText("Batch execution scheduled.")).toBeVisible();
  await expect(page.getByRole("link", { name: /created-run-\d+/ })).toBeVisible();

  await page.getByRole("link", { name: /created-run-\d+/ }).first().click();

  await expect(page.getByRole("heading", { name: /Evaluation Run created-run-/ })).toBeVisible();
  await expect(page.getByRole("heading", { name: "Artifact Viewer" })).toBeVisible();
  await expect(page.getByRole("heading", { name: "Raw Trace" })).toBeVisible();
  await expect(page.getByText("Smoke harness verdict generated by the monitor Playwright fixture.").first()).toBeVisible();
  await expect(page.getByText("Inspecting repository checkout")).toBeVisible();
  await expect(page.getByText("model_patch.diff")).toBeVisible();
});

test("shows regression compare output and downloads a live export payload", async ({ page }) => {
  await page.goto("/evaluation");

  await page.getByLabel("Baseline batch").selectOption("batch-candidate");
  await page.getByLabel("Candidate batch").selectOption("batch-baseline");
  await page.getByRole("button", { name: "Compare batches" }).click();

  await expect(page.getByText("Regression detected")).toBeVisible();
  await expect(page.getByRole("cell", { name: "Pass Rate" })).toBeVisible();
  await expect(page.getByRole("cell", { name: "-50.0%" }).first()).toBeVisible();

  await page.goto("/evaluation/batches/batch-candidate");
  const downloadPromise = page.waitForEvent("download");
  await page.getByRole("button", { name: "Download export" }).click();
  const download = await downloadPromise;

  expect(download.suggestedFilename()).toBe("batch-candidate-predictions_jsonl.json");
  await expect(page.getByText("Downloaded batch-candidate-predictions_jsonl.json")).toBeVisible();
  await expect(page.getByText("\"run_records\"")).toBeVisible();
});

test("renders backend failures instead of hanging when a run is missing", async ({ page }) => {
  await page.goto("/evaluation/runs/missing-run");

  await expect(page.getByRole("heading", { name: "Evaluation run missing-run: Request failed" })).toBeVisible();
  await expect(page.getByText("Evaluation run not found: missing-run")).toBeVisible();
});
